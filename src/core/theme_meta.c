ThemeId
NormalizeTheme(int theme)
{
    if(theme < 0 || theme >= THEME_COUNT)
        return THEME_SUNSET;
    return (ThemeId)theme;
}

const ThemeMeta *
GetThemeMeta(ThemeId theme)
{
    return &themes[NormalizeTheme(theme)];
}

const char *
GetThemeLabel(ThemeId theme)
{
    return GetThemeMeta(theme)->name;
}

const char *
GetThemeScopeName(ThemeId theme, bool dark_mode)
{
    const ThemeMeta *meta = GetThemeMeta(theme);
    return dark_mode ? meta->dark_scope : meta->light_scope;
}

static bool
catalog_value(const ThemeCatalogColors *colors, const char *key, Color *color)
{
    if(key == NULL || color == NULL)
        return false;

    if(strcmp(key, "background") == 0)
        *color = colors->background;
    else if(strcmp(key, "surface") == 0)
        *color = colors->surface;
    else if(strcmp(key, "text") == 0)
        *color = colors->text;
    else if(strcmp(key, "circle") == 0)
        *color = colors->circle;
    else if(strcmp(key, "button") == 0)
        *color = colors->button;
    else if(strcmp(key, "button_hover") == 0)
        *color = colors->button_hover;
    else if(strcmp(key, "icon") == 0)
        *color = colors->icon;
    else if(strcmp(key, "link") == 0)
        *color = colors->link;
    else
        return false;

    return true;
}

bool
GetThemeCatalogColor(ThemeId theme, bool dark_mode, const char *key, Color *color)
{
    ThemeId id = NormalizeTheme(theme);
    return catalog_value(dark_mode ? &catalog_dark[id] : &catalog_light[id], key, color);
}

bool
GetThemeCatalogScopeColor(const char *scope, const char *key, Color *color)
{
    if(scope == NULL)
        return false;

    for(int i = 0; i < THEME_COUNT; i++) {
        if(strcmp(scope, themes[i].light_scope) == 0)
            return GetThemeCatalogColor((ThemeId)i, false, key, color);
        if(strcmp(scope, themes[i].dark_scope) == 0)
            return GetThemeCatalogColor((ThemeId)i, true, key, color);
    }

    return false;
}

#!/bin/sh
set -eu

icon_dir=${1:-icons}
out=${2:-src/ui/ui_icon_assets.c}
tmp="${out}.tmp"

primary_icon_dir=${icon_dir%% *}
icon_files=$(
    for dir in $icon_dir; do
        find "$dir" -maxdepth 1 -type f -name '*.png' 2>/dev/null
    done | LC_ALL=C sort
)

icon_ident()
{
    printf '%s' "$1" | sed 's/[^A-Za-z0-9]/_/g'
}

icon_enum()
{
    icon_ident "$1" | tr '[:lower:]' '[:upper:]'
}

icon_name()
{
    file=$1
    dir=${file%/*}
    base=$(basename "$file" .png)
    if [ "$dir" = "$primary_icon_dir" ]; then
        printf '%s' "$base"
    else
        printf '%s_%s' "$(basename "$dir")" "$base"
    fi
}

{
    printf '/* Generated by scripts/embed-icons.sh. */\n'
    printf '#include "ui_icons.h"\n\n'

    for file in $icon_files; do
        name=$(icon_name "$file")
        ident=$(icon_ident "$name")

        printf 'static const unsigned char ui_icon_%s[] = {\n' "$ident"
        od -An -v -tx1 "$file" | awk '
        {
            printf "   ";
            for(i = 1; i <= NF; i++)
                printf " 0x%s,", $i;
            printf "\n";
        }'
        printf '};\n\n'
    done

    printf 'const UIIconAsset ui_icon_assets[] = {\n'
    count=0
    for file in $icon_files; do
        name=$(icon_name "$file")
        ident=$(icon_ident "$name")
        enum=$(icon_enum "$name")
        size=$(wc -c < "$file" | tr -d ' ')
        printf '    {UI_ICON_TYPE_%s, "%s", ui_icon_%s, %su},\n' "$enum" "$name" "$ident" "$size"
        count=$((count + 1))
    done
    printf '};\n\n'
    printf 'const unsigned int ui_icon_asset_count = %su;\n' "$count"
} > "$tmp"

# Generate enum definitions
enum_out="include/ui_icon_types.h"
enum_tmp="${enum_out}.tmp"
{
    printf '/* Generated by scripts/embed-icons.sh. DO NOT EDIT MANUALLY. */\n'
    printf '#ifndef UI_ICON_TYPES_H\n'
    printf '#define UI_ICON_TYPES_H\n\n'
    printf 'typedef enum {\n'
    printf '    UI_ICON_TYPE_NONE = 0,\n\n'
    count=0
    for file in $icon_files; do
        name=$(icon_name "$file")
        enum=$(icon_enum "$name")
        printf '    UI_ICON_TYPE_%s,\n' "$enum"
        count=$((count + 1))
    done
    printf '\n    UI_ICON_TYPE_COUNT = %d\n' "$((count + 1))"
    printf '} UIIconType;\n\n'
    printf '#endif /* UI_ICON_TYPES_H */\n'
} > "$enum_tmp"

if [ -f "$enum_out" ] && cmp -s "$enum_tmp" "$enum_out"; then
    rm "$enum_tmp"
else
    mv "$enum_tmp" "$enum_out"
    echo "Updated $enum_out with $count icon types"
fi

# Generate icon names array (matching enum order)
names_out="src/ui/ui_icon_names.c"
names_tmp="${names_out}.tmp"
{
    printf '/* Generated by scripts/embed-icons.sh. DO NOT EDIT MANUALLY. */\n'
    printf '#include <stddef.h>\n'
    printf '#include "ui_icon_types.h"\n\n'
    printf 'const char *ui_icon_names[] = {\n'
    printf '    NULL,  /* UI_ICON_TYPE_NONE */\n'
    for file in $icon_files; do
        name=$(icon_name "$file")
        printf '    "%s.png",  /* UI_ICON_TYPE_%s */\n' "$name" "$(icon_enum "$name")"
    done
    printf '};\n'
} > "$names_tmp"

if [ -f "$names_out" ] && cmp -s "$names_tmp" "$names_out"; then
    rm "$names_tmp"
else
    mv "$names_tmp" "$names_out"
    echo "Updated $names_out with $count icon names"
fi

if [ -f "$out" ] && cmp -s "$tmp" "$out"; then
    rm "$tmp"
else
    mv "$tmp" "$out"
fi

#!/bin/sh
set -eu

raylib_header=${1:-vendor/raylib/src/raylib.h}
public_header=${2:-include/flint_compat.generated.h}
rename_header=${3:-build/generated/raylib_backend_rename.h}
wrapper_source=${4:-build/generated/flint_raylib_wrappers.c}
backend_prefix=${FLINT_RAYLIB_BACKEND_PREFIX:-FlintRaylibBackend_}

if [ ! -f "$raylib_header" ]; then
    echo "raylib header not found: $raylib_header" >&2
    exit 1
fi

mkdir -p "$(dirname "$public_header")" "$(dirname "$rename_header")" "$(dirname "$wrapper_source")"

{
    echo "/* Generated by tools/generate-flint-compat.sh; do not edit. */"
    echo "/* Flint exposes this Raylib-compatible surface through kryon.h. */"
    cat "$raylib_header"
} > "$public_header"

{
    echo "/* Generated by tools/generate-flint-compat.sh; do not edit. */"
    echo "#ifndef FLINT_RAYLIB_BACKEND_RENAME_H"
    echo "#define FLINT_RAYLIB_BACKEND_RENAME_H"
    echo
    awk '
        /^[[:space:]]*(RLAPI|RMAPI)[[:space:]]/ {
            line = $0
            sub(/\(.*/, "", line)
            n = split(line, parts, /[[:space:]\*]+/)
            if(n > 0 && parts[n] ~ /^[A-Za-z_][A-Za-z0-9_]*$/)
                print parts[n]
        }
    ' "$raylib_header" | sort -u | while IFS= read -r symbol; do
        [ -n "$symbol" ] || continue
        printf '#define %s %s%s\n' "$symbol" "$backend_prefix" "$symbol"
    done
    echo
    echo "#endif"
} > "$rename_header"

awk -v prefix="$backend_prefix" '
function trim(s) {
    sub(/^[[:space:]]+/, "", s)
    sub(/[[:space:]]+$/, "", s)
    return s
}

function function_name_from_prefix(prefix_text, parts, n) {
    n = split(prefix_text, parts, /[[:space:]\*]+/)
    return parts[n]
}

function return_type_from_prefix(prefix_text, name) {
    sub(/^[[:space:]]*(RLAPI|RMAPI)[[:space:]]+/, "", prefix_text)
    sub("[[:space:]]*" name "$", "", prefix_text)
    return trim(prefix_text)
}

function arg_name(arg, parts, n, name) {
    arg = trim(arg)
    sub(/=.*/, "", arg)
    gsub(/\[[^]]*\]/, "", arg)
    n = split(arg, parts, /[[:space:]\*]+/)
    name = parts[n]
    gsub(/[^A-Za-z0-9_]/, "", name)
    return name
}

function call_args(args, parts, count, i, name, out) {
    args = trim(args)
    if(args == "void" || args == "")
        return ""
    count = split(args, parts, /,[[:space:]]*/)
    out = ""
    for(i = 1; i <= count; i++) {
        if(parts[i] == "...")
            continue
        name = arg_name(parts[i])
        if(name == "")
            continue
        out = out (out == "" ? "" : ", ") name
    }
    return out
}

function emit_regular_wrapper(return_type, name, args, backend_args, returns_void) {
    print "extern " return_type " " prefix name "(" args ");"
    print return_type " " name "(" args ")"
    print "{"
    if(returns_void) {
        print "    " prefix name "(" backend_args ");"
    } else {
        print "    return " prefix name "(" backend_args ");"
    }
    print "}"
    print ""
}

function emit_variadic_wrapper(return_type, name, args, fixed_args, backend_args) {
    print "extern " return_type " " prefix name "(" args ");"
    print return_type " " name "(" args ")"
    print "{"
    if(name == "TextFormat")
        print "    static char text_buffer[4096];"
    else
        print "    char text_buffer[4096];"
    print "    va_list ap;"
    print "    va_start(ap, text);"
    print "    vsnprintf(text_buffer, sizeof(text_buffer), text, ap);"
    print "    va_end(ap);"
    if(name == "TraceLog") {
        print "    " prefix name "(logLevel, \"%s\", text_buffer);"
    } else {
        print "    (void)" prefix name ";"
        print "    return text_buffer;"
    }
    print "}"
    print ""
}

BEGIN {
    print "/* Generated by tools/generate-flint-compat.sh; do not edit. */"
    print "#include \"kryon.h\""
    print "#include <stdarg.h>"
    print "#include <stdio.h>"
    print ""
}

/^[[:space:]]*(RLAPI|RMAPI)[[:space:]]/ {
    line = $0
    sub(/\/\/.*$/, "", line)
    line = trim(line)
    sub(/;$/, "", line)
    prefix_text = line
    sub(/\(.*/, "", prefix_text)
    name = function_name_from_prefix(prefix_text)
    return_type = return_type_from_prefix(prefix_text, name)
    args = line
    sub(/^[^(]*\(/, "", args)
    sub(/\)$/, "", args)
    if(args ~ /\.\.\./) {
        emit_variadic_wrapper(return_type, name, args)
    } else {
        emit_regular_wrapper(return_type, name, args, call_args(args), return_type == "void")
    }
}
' "$raylib_header" > "$wrapper_source"

#include "ui_icons.h"
#include "ui_internal.h"
#include "flint.h"
#include <math.h>
#include <stdio.h>
#include <string.h>

extern const UIIconAsset ui_icon_assets[];
extern const unsigned int ui_icon_asset_count;
extern const char *ui_icon_names[];  /* Auto-generated by embed-icons.sh */

const UIIconAsset *
GetUIIconAsset(UIIconType type)
{
    for(unsigned int i = 0; i < ui_icon_asset_count; i++) {
        if(ui_icon_assets[i].type == type)
            return &ui_icon_assets[i];
    }
    return NULL;
}

const UIIconAsset *
GetUIIconAssetByName(const char *name)
{
    if(name == NULL)
        return NULL;

    for(unsigned int i = 0; i < ui_icon_asset_count; i++) {
        if(strcmp(ui_icon_assets[i].name, name) == 0)
            return &ui_icon_assets[i];
    }
    return NULL;
}

static Texture2D
load_icon_asset_texture(const UIIconAsset *asset)
{
    Texture2D texture = {0};
    Image image;

    if(asset == NULL || asset->data == NULL || asset->size == 0)
        return texture;

    image = LoadImageFromMemory(".png", asset->data, (int)asset->size);
    if(image.data == NULL)
        return texture;

    texture = LoadTextureFromImage(image);
    UnloadImage(image);
    if(texture.id != 0)
        SetTextureFilter(texture, TEXTURE_FILTER_POINT);
    return texture;
}

Texture2D
LoadUIIconTexture(UIIconType type)
{
    return load_icon_asset_texture(GetUIIconAsset(type));
}

Texture2D
LoadUIIconTextureByName(const char *name)
{
    return load_icon_asset_texture(GetUIIconAssetByName(name));
}

void
LoadAllUIIconTextures(Texture2D *icons)
{
    if(icons == NULL)
        return;

    for(int i = 1; i < UI_ICON_TYPE_COUNT; i++) {
        if(ui_icon_names[i] != NULL && icons[i].id == 0) {
            char icon_name[64];
            char *ext;
            snprintf(icon_name, sizeof(icon_name), "%s", ui_icon_names[i]);
            ext = strrchr(icon_name, '.');
            if(ext != NULL && strcmp(ext, ".png") == 0)
                *ext = '\0';
            icons[i] = LoadUIIconTextureByName(icon_name);
        }
    }
}

void
UnloadAllUIIconTextures(Texture2D *icons)
{
    if(icons == NULL)
        return;

    for(int i = 0; i < UI_ICON_TYPE_COUNT; i++) {
        if(icons[i].id != 0) {
            UnloadTexture(icons[i]);
            icons[i].id = 0;
        }
    }
}
